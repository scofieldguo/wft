public class WftOpenAcessCardStat {
	private String dairy;
	private String channel;
	private Integer cmcccnt;
	private Integer chcnt;
	private String hour;
	public String getDairy() {
		return dairy;
	}
	public void setDairy(String dairy) {
		this.dairy = dairy;
	}
	public String getChannel() {
		return channel;
	}
	public void setChannel(String channel) {
		this.channel = channel;
	}
	public Integer getCmcccnt() {
		return cmcccnt;
	}
	public void setCmcccnt(Integer cmcccnt) {
		this.cmcccnt = cmcccnt;
	}
	public Integer getChcnt() {
		return chcnt;
	}
	public void setChcnt(Integer chcnt) {
		this.chcnt = chcnt;
	}
	public String getHour() {
		return hour;
	}
	public void setHour(String hour) {
		this.hour = hour;
	}
	
}
